


void main() {
	
	
	int x = 4;

	do { 


		print("1 : C");
		print("2 : CPP");
		print("3 : Java");
		print("4 : Dart");

		switch(x) { 


			case 1 : print("C");

			case 2 : print("CPP");

			case 3 : print("Java");

			case 4 : print("Dart");

			default : print("No Problem");
	
		}

	}while(false);	

}
